
public class Karte {
	private int wert;
	private int farbe;
	
	public Karte(int wert, int farbe){
		this.wert = wert;
		this.farbe = farbe;
	}

	
	public int getWert() {
		return wert;
	}

	public void setWert(int wert) {
		this.wert = wert;
	}

	public int getFarbe() {
		return farbe;
	}

	public void setFarbe(int farbe) {
		this.farbe = farbe;
	}
	
}
